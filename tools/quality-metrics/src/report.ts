#!/usr/bin/env node
/**
 * Quality Metrics Reporter
 * 
 * Generates various report formats:
 * - Markdown report for PRs and documentation
 * - JSON report for CI/CD integration
 * - HTML report for dashboard
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';

interface QualityMetrics {
  timestamp: string;
  coverage: {
    lines: number;
    functions: number;
    branches: number;
    statements: number;
  };
  build: {
    duration: number;
    cacheHitRate: number;
    bundleSize: number;
  };
  codeQuality: {
    typeErrors: number;
    lintWarnings: number;
    lintErrors: number;
    todoCount: number;
    filesCount: number;
    linesOfCode: number;
  };
  git: {
    commitCount: number;
    contributors: number;
    lastCommitDate: string;
    filesChanged: number;
  };
}

function loadLatestMetrics(): QualityMetrics {
  const workspaceRoot = join(process.cwd(), '..', '..');
  const latestPath = join(workspaceRoot, '.quality-metrics', 'latest.json');
  
  if (!existsSync(latestPath)) {
    console.error('‚ùå No metrics found. Run `pnpm collect` first.');
    process.exit(1);
  }

  return JSON.parse(readFileSync(latestPath, 'utf-8'));
}

