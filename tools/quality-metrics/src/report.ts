async function main() {
  const format = process.argv[2] || 'markdown';
  const metrics = loadLatestMetrics();

  let report: string;
  let extension: string;

  switch (format) {
    case 'json':
      report = generateJSONReport(metrics);
      extension = 'json';
      break;
    case 'html':
      report = generateHTMLReport(metrics);
      extension = 'html';
      break;
    case 'markdown':
    default:
      report = generateMarkdownReport(metrics);
      extension = 'md';
      break;
  }

  const workspaceRoot = join(process.cwd(), '..', '..');
  const reportPath = join(workspaceRoot, '.quality-metrics', `report.${extension}`);
  writeFileSync(reportPath, report);

  console.log(`‚úÖ ${format.toUpperCase()} report generated: ${reportPath}`);
  
  if (format === 'markdown') {
    console.log('\n' + report);
  }
}

main();
